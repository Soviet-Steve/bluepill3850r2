/* USER CODE BEGIN 4 */
int startEcho()
{
  HAL_GPIO_WritePin(SONAR_ECHO_GPIO_Port, SONAR_TRIG_Pin, GPIO_PIN_SET);
  ultraCounter = 0;
  sonicStartTime = time;
  sonicState = IN_PROGRESS;
  return 0;
}

int stopEcho()
{
  HAL_GPIO_WritePin(SONAR_ECHO_GPIO_Port, SONAR_ECHO_Pin, GPIO_PIN_RESET);
  return 0;
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  sonicStopTime = time;
  sonicState = NOT_IN_PROGRESS;
  distance = (sonicStopTime - sonicStartTime) * MEGA * SPEED_OF_SOUND;
}
